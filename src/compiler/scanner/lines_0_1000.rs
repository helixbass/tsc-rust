pub(super) fn lookup_in_unicode_map(code: char, map: &[char]) -> bool {
    if code < map[0] {
        return false;
    }

    let mut lo = 0;
    let mut hi = map.len();
    let mut mid: usize;

    while lo + 1 < hi {
        mid = lo + (hi - lo) / 2;
        mid -= mid % 2;
        if map[mid] <= code && code <= map[mid + 1] {
            return true;
        }

        if code < map[mid] {
            hi = mid;
        } else {
            lo = mid + 2;
        }
    }

    false
}

